# 1. 导入工具函数
# 从已安装的 'python-dotenv' 库中，引入 'load_dotenv' 函数。
# 这个函数的唯一任务就是：读取磁盘上的 .env 文件，并将其中的内容“注入”到当前程序的环境变量中。
from dotenv import load_dotenv

# 2. 导入系统模块
# 导入 Python 内置的 'os' 模块。
# 它是程序与操作系统交互的桥梁，这里主要用它提供的 'getenv' 方法来读取环境变量。
import os

# 3. 执行加载动作 (关键步骤！)
# 调用 load_dotenv() 函数。
# 【发生了什么】：
#   - 程序会自动在当前目录下寻找名为 '.env' 的文件。
#   - 如果找到，它会逐行解析文件内容（例如：TESTPASS=cisc0123）。
#   - 然后，它将这些键值对写入到当前 Python 进程的【内存环境变量表】中。
# 【注意】：如果不执行这一行，后续的 os.getenv 将读不到 .env 文件里的内容，只会读到终端里手动设置的变量。
load_dotenv()  

# 4. 读取并打印变量
# 【第一步：查询】
#   os.getenv("TESTPASS") 会去刚才加载好的【内存环境变量表】中查找键为 "TESTPASS" 的值。
#   - 如果找到了（因为上面加载了 .env），它返回字符串 "cisc0123"。
#   - 如果没找到（比如 .env 没保存或文件名错了），它默认返回 None。
#
# 【第二步：输出】
#   print() 函数将获取到的值打印到控制台屏幕上。
#
# ⚠️ 安全警告：
# 在实际生产代码中，千万不要像这样直接把密码打印出来！
# 这行代码仅用于测试验证配置是否读取成功。正式项目中应直接使用变量，避免泄露敏感信息。
print(os.getenv("TESTPASS"))  